    <h2 class="reversebannertagline">Audience Description</h2>
    <h4>2013 USA Triathlon Membership Report</h4>
        <p>Growth in participation in triathlons can be attributed to: society’s interest in fitness and living a healthy lifestyle, the growth of the number of total races across the country, making races easier to get to, the growth in the number of the more accessible shorter sprint races, which made the sport more accessible to those with fewer hours to train each week, media attention on the sport, hrowth in the 30-49 age groups who are looking for varied outlets for fitness, peer pressure from friends who have tried the sport, the ego reward of saying you “are a triathlete”, increase in clubs, which create a community concept for men and especially women who enjoy the group training and support atmosphere, increase in resources (websites, books, magazines) that provide assistance/education in getting started, growth in multisport shops and triathlon specific training and racing gear, marketing and communications efforts of USA Triathlon, growth in the number of USA Triathlon certified coaches who are able to provide training plans and individual attention for athletes who need guidance and motivation.</p>
        <p>On the event side, the number of races sanctioned through USA Triathlon continues to climb. Those numbers (which include camps and clinics) have nearly tripled over the last seven years, going from 1,541 in 2004 to 4,327 in 2013.  Looking at the distances of the races, the biggest growth continues to be at the shorter sprint distances, which have surged from 818 in 2004 to 1,507 in 2010, but growth at other distances (including Olympic, Ironman 70.3 and Ironman) has also been consistent during that time.  Additionally, a number of events are not sanctioned by USA Triathlon are put on across the United States each year. The majority of these are offered through organizations with insurance and liability programs already in place, such as universities and YMCAs.</p>
        <p>The sport has consistently grown and continues to thrive in a number of areas. People are choosing to be more active, which leads more people to join in the sport as annual members of USA Triathlon. But the better news is that people are sticking around, doing more events and becoming lifelong members of the multisport community.</p>
        <p>Who are Triathletes?  In the late 1990s there was a slow increase in overall membership but a sizeable shift in the participation base from 30-34 to 35-39. Since that time the greatest growth has occurred in the 35-39 and 40-44 age groups. We believe that this growth will continue as those in these age groups are looking for new outlets of participation and fitness. Fortunately growth has been good in all age categories in the past decade and so the sport as a whole should continue to increase as more events are being started in smaller triathlon markets.</p>
        <p>Female Participation:  Since 2000, USA Triathlon's female membership has grown from 27 percent of the total of the annual members to more than 36.5 percent at the end of 2013. Factors leading to this growth are society’s acceptance of “active” women, women feeling more comfortable living an active lifestyle, the growth of women’s-only events like the Danskin and Trek Triathlon Series, and races focusing on charity involvement and fundraising.</p>
        <p>Youth Growth:  USA Triathlon categorizes youth athletes as ages 7-17. At present, youth triathlon is still in a very organic, grassroots state. Many — if not most — races are organized by groups that already service young populations. We are seeing an increase in the number of “adult” triathlons that are adding youth events to their program in what is commonly called a “triathlon festival.” The idea is to provide a menu of events over the course of a weekend, from sprint to long course distances, to attract as many people as possible. Race directors have realized a growing demand to provide something for the young kids who may join mom and dad at the triathlon festival. In this way, triathlon is evolving into a multi-generational sporting event … all taking place on the same weekend.</p>
        <p>Today’s younth also have the advantage of recognizing triathlon as a “real” sport. It is not a fringe sport to them as it was when many adults got their start in the late-80s and early-90s. The sport is televised more often — including broadcasts on NBC, Universal Sports Network and regional sports networks — and the web has made information much more accessible than it was even in 2000 when triathlon debuted in the Olympics.</p>
        <p>A study initiated by USA Triathlon and conducted by the TribeGroup in early 2009 revealed exciting new data on the demographics and spending habits of multisport athletes in the United States.</p>
        <p>Key Findings:  The study grouped participants into seven distinct segments according to similarities in their demographics, psychographics and involvement in the sport. On average, triathletes are from high socio-economic backgrounds with median incomes of $126,000, however, their motivators and mindset are different, creating a significant opportunity for the triathlon community to serve them better and in more targeted ways. Unlike many consumers, they are spending cautiously but on average intend to spend as much in 2009 as they did in 2008. In fact, 45 percent would rather spend discretionary income on triathlon with three of seven segments exceeding 65 percent and one nearly 80 percent.</p>
        <p>General Participation:  Older athletes do more races 95% participate for the personal challenge 87% participate to stay in shape On average, they participated in 4.2 triathlons during the previous 12 months 86% plan to do longer races in the future Sprint triathlon remains the most popular, with those events attracting participation of more than three quarters of respondents</p>
        <p>What Distances?  78% participate in Sprint (approx. 500 meter swim, 12 mile bike, 5k run) 58% participate in Olympic (1.5k swim, 40k bike, 10k run) 39% participate in Half Ironman (1.2 mile swim, 56 mile bike, 13.1 mile run) 17% participate in Ironman (2.4 mile swim, 112 mile bike, 26.2 mile run) 15% participate in Short duathlon (approx. 5k run, 30k bike, 5k run) 4% participate in Long duathlon (approx. 10k run, 70k bike, 10k run)</p>

